# Logical operators
# and, or , not

# "and" operator

print(2 == 2 and 3 == 3)
print(2 == 2 and 3 == 4)
print(1!= 1 and 3 == 3)
print( 3 < 2 and 4 > 5)

# "or" operator

print(2 == 2 or 4 == 4)
print(2 == 2 or 4 == 5)
print(3 < 2 or 4 > 7)

# "not" operator
print(not 4 == 4)
print(not 9 > 12)